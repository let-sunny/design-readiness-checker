/**
 * Node screenshot for analyze --visual (Figma preview only, no comparison)
 */
export interface NodeScreenshot {
  nodeId: string;
  nodePath: string;
  screenshotBase64: string;
  issueCount: number;
  topSeverity: string;
}

export interface HtmlReportOptions {
  visualComparisons?: VisualComparisonRecord[];
  nodeScreenshots?: NodeScreenshot[];
}

/**
 * Generate a static HTML report with optional visual sections
 */
export function generateHtmlReport(
  file: AnalysisFile,
  result: AnalysisResult,
  scores: ScoreReport,
  options?: HtmlReportOptions
): string {
  const visualComparisons = options?.visualComparisons;
  const nodeScreenshots = options?.nodeScreenshots;
  const quickWins = getQuickWins(result.issues, 5);
  const issuesByCategory = groupIssuesByCategory(result.issues);

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Design Readiness Report - ${escapeHtml(file.name)}</title>
  <style>
${getStyles()}
  </style>
</head>
<body>
  <div class="container">
    <header>
      <h1>Design Readiness Report</h1>
      <p class="file-name">${escapeHtml(file.name)}</p>
      <p class="meta">Generated: ${new Date().toLocaleString()} | Nodes: ${result.nodeCount} | Max Depth: ${result.maxDepth}</p>
    </header>

    <section class="overall-score">
      <div class="grade grade-${scores.overall.grade}">${scores.overall.grade}</div>
      <div class="score-details">
        <div class="percentage">${scores.overall.percentage}%</div>
        <div class="score-label">Overall Score</div>
      </div>
    </section>

    <section class="summary">
      <h2>Issue Summary</h2>
      <div class="summary-grid">
        <div class="summary-item severity-blocking">
          <span class="count">${scores.summary.blocking}</span>
          <span class="label">Blocking</span>
        </div>
        <div class="summary-item severity-risk">
          <span class="count">${scores.summary.risk}</span>
          <span class="label">Risk</span>
        </div>
        <div class="summary-item severity-missing-info">
          <span class="count">${scores.summary.missingInfo}</span>
          <span class="label">Missing Info</span>
        </div>
        <div class="summary-item severity-suggestion">
          <span class="count">${scores.summary.suggestion}</span>
          <span class="label">Suggestion</span>
        </div>
      </div>
    </section>

    <section class="categories">
      <h2>Category Scores</h2>
      <div class="category-grid">
${CATEGORIES.map(cat => renderCategoryCard(cat, scores)).join("\n")}
      </div>
    </section>

${quickWins.length > 0 ? renderQuickWins(quickWins, file.fileKey) : ""}

    <section class="issues">
      <h2>All Issues (${result.issues.length})</h2>
${CATEGORIES.map(cat => renderCategoryIssues(cat, issuesByCategory.get(cat) ?? [], file.fileKey)).join("\n")}
    </section>

${nodeScreenshots && nodeScreenshots.length > 0 ? renderNodeScreenshotSection(nodeScreenshots, file.fileKey) : ""}
${visualComparisons && visualComparisons.length > 0 ? renderVisualComparisonSection(visualComparisons) : ""}

    <footer>
      <p>Generated by <strong>Design Readiness Checker</strong></p>
    </footer>
  </div>
</body>
</html>`;
}

