function renderCategoryCard(category: Category, scores: ScoreReport): string {
  const cat = scores.byCategory[category];
  const scoreColor = cat.percentage >= 80 ? "#16a34a" : cat.percentage >= 60 ? "#ca8a04" : "#dc2626";

  return `        <div class="category-card">
          <h3>${CATEGORY_LABELS[category]}</h3>
          <div class="score" style="color: ${scoreColor}">${cat.percentage}%</div>
          <div class="issues">${cat.issueCount} issues</div>
        </div>`;
}

function renderQuickWins(issues: AnalysisIssue[], fileKey: string): string {
  return `
    <section class="quick-wins">
      <h2>Quick Wins - Top Blocking Issues</h2>
      <p style="margin-bottom: 1rem; color: #92400e;">Fix these high-impact issues first for the biggest improvement.</p>
      <ul class="issue-list">
${issues.map(issue => renderIssueItem(issue, fileKey)).join("\n")}
      </ul>
    </section>`;
}

function renderCategoryIssues(
  category: Category,
  issues: AnalysisIssue[],
  fileKey: string
): string {
  return `      <div class="category-section">
        <h3>${CATEGORY_LABELS[category]} (${issues.length})</h3>
        ${issues.length === 0
          ? '<p class="no-issues">No issues found</p>'
          : `<ul class="issue-list">\n${issues.map(issue => renderIssueItem(issue, fileKey)).join("\n")}\n        </ul>`
        }
      </div>`;
}

function renderIssueItem(issue: AnalysisIssue, fileKey: string): string {
  const severity = issue.config.severity;
  const def = issue.rule.definition;
  const figmaLink = buildFigmaDeepLink(fileKey, issue.violation.nodeId);

  return `        <li class="issue-item">
          <div class="issue-header">
            <span class="severity-badge badge-${severity}">${SEVERITY_LABELS[severity]}</span>
            <span class="issue-message">${escapeHtml(issue.violation.message)}</span>
          </div>
          <div class="issue-path">${escapeHtml(issue.violation.nodePath)}</div>
          <div class="issue-details">
            <p><strong>Why:</strong> ${escapeHtml(def.why)}</p>
            <p><strong>Impact:</strong> ${escapeHtml(def.impact)}</p>
            <p><strong>Fix:</strong> ${escapeHtml(def.fix)}</p>
          </div>
          <a href="${figmaLink}" target="_blank" rel="noopener" class="figma-link">Open in Figma</a>
        </li>`;
}

function getQuickWins(issues: AnalysisIssue[], limit: number): AnalysisIssue[] {
  // Get blocking issues sorted by score impact (most negative first)
  return issues
    .filter(issue => issue.config.severity === "blocking")
    .sort((a, b) => a.calculatedScore - b.calculatedScore)
    .slice(0, limit);
}

function groupIssuesByCategory(issues: AnalysisIssue[]): Map<Category, AnalysisIssue[]> {
  const grouped = new Map<Category, AnalysisIssue[]>();

  for (const category of CATEGORIES) {
    grouped.set(category, []);
  }

  for (const issue of issues) {
    const category = issue.rule.definition.category;
    grouped.get(category)!.push(issue);
  }

  return grouped;
}

function renderNodeScreenshotSection(screenshots: NodeScreenshot[], fileKey: string): string {
  const cards = screenshots.map((ns) => {
    const badgeClass = ns.topSeverity === "blocking" ? "badge-blocking" : "badge-risk";
    const figmaLink = buildFigmaDeepLink(fileKey, ns.nodeId);

    return `<div class="ns-card">
        <img src="data:image/png;base64,${ns.screenshotBase64}" alt="${escapeHtml(ns.nodePath)}">
        <h4>${escapeHtml(ns.nodePath)}</h4>
        <div class="ns-meta">
          <span class="severity-badge ${badgeClass}">${escapeHtml(ns.topSeverity)} (${ns.issueCount})</span>
          <a href="${figmaLink}" target="_blank" rel="noopener" class="figma-link">Open in Figma</a>
        </div>
      </div>`;
  });

  return `
    <section>
      <h2>Node Previews (${screenshots.length})</h2>
      <p style="margin-bottom:1rem;color:#666;font-size:0.875rem">Figma screenshots of the most impactful nodes with blocking or risk issues.</p>
      <div class="node-screenshots-grid">
${cards.join("\n")}
      </div>
    </section>`;
}

function renderVisualComparisonSection(comparisons: VisualComparisonRecord[]): string {
  const cards = comparisons.map((vc) => {
    const similarity = 100 - vc.pixelComparison.pixelDiffPercentage;
    const cssClass = similarity >= 70 ? "good" : similarity >= 40 ? "warn" : "poor";
    const sizeNote = vc.pixelComparison.sizeMatch ? "" : " (size mismatch)";

    const deepSection = vc.deepComparison
      ? `<div class="vc-deep">
          <p><strong>AI Analysis:</strong> ${vc.deepComparison.similarityScore}% similarity</p>
          ${vc.deepComparison.diffAreas.length > 0 ? `<p><strong>Diff Areas:</strong> ${vc.deepComparison.diffAreas.map(a => escapeHtml(a)).join("; ")}</p>` : ""}
          ${vc.deepComparison.causeRuleIds.length > 0 ? `<p><strong>Cause Rules:</strong> ${vc.deepComparison.causeRuleIds.map(r => `<code>${escapeHtml(r)}</code>`).join(", ")}</p>` : ""}
        </div>`
      : "";

    const diffImage = vc.pixelComparison.diffImageBase64
      ? `<div class="vc-image-box">
          <span class="vc-label">Diff</span>
          <img src="data:image/png;base64,${vc.pixelComparison.diffImageBase64}" alt="Diff">
        </div>`
      : "";

    return `<div class="vc-card">
        <h3>${escapeHtml(vc.nodePath)} <span style="color:#999;font-weight:normal">(${escapeHtml(vc.nodeId)})</span></h3>
        <div class="vc-images">
          <div class="vc-image-box">
            <span class="vc-label">Figma Original</span>
            <img src="data:image/png;base64,${vc.figmaScreenshotBase64}" alt="Figma">
          </div>
          <div class="vc-image-box">
            <span class="vc-label">AI Implementation</span>
            <img src="data:image/png;base64,${vc.renderedScreenshotBase64}" alt="Rendered">
          </div>
        </div>
        <div class="vc-similarity ${cssClass}">Similarity: ${similarity.toFixed(1)}%${sizeNote}</div>
        <div class="vc-details">
          <p>Diff pixels: ${vc.pixelComparison.diffPixels} / ${vc.pixelComparison.totalPixels} (${vc.pixelComparison.width}x${vc.pixelComparison.height})</p>
        </div>
        ${diffImage}
        ${deepSection}
      </div>`;
  });

  return `
    <section>
      <h2>Visual Comparison (${comparisons.length})</h2>
      <div class="visual-comparison-grid">
${cards.join("\n")}
      </div>
    </section>`;
}

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}
